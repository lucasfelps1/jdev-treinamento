package cursojava.fundamentos;

public class TipoChar {

	public static void main(TipoStrings[] args) {
		
		/* Char representa uma letra */
		
		char pessoaFisica = 'F';
		
		char pessoaJuridica = 'J';
		
		char pessoaMasculino = 'M';
		
		char pessoaFeminino = 'F';
		
		if(pessoaFeminino == 'F') {
			System.out.println("Feminino");
		} else {
			System.out.println("Masculino");
		}
		
	}
	
}
